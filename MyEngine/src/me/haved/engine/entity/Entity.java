public class Entity
{
	private int x, y;
	
	public Entity()
	{
		
	}
	
	public Entity(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
	
	public void update()
	{
		
	}
	
	public void render()
	{
		
	}
	
	public int getX()
	{
		return x;
	}

	public void setX(int x)
	{
		this.x = x;
	}

	public int getY()
	{
		return y;
	}

	public void setY(int y)
	{
		this.y = y;
	}
}
